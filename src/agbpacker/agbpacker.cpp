#include <iostream>
#include <stdexcept>

import argpppp;

int main(int argc, char** argv)
{
    try
    {
        argpppp::parser parser;
        parser.doc("Compress and decompress data for the GBA BIOS");
        parser.parse(argc, argv);
        return EXIT_SUCCESS;
    }
    catch (const std::exception& e)
    {
        std::cerr << argpppp::program_name(argv[0]) << ": " << e.what() << "\n";
        return EXIT_FAILURE;
    }
}
